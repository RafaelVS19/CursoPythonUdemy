"""
Tipos de dados:
str - string - textos 'Assim' "Assim"
int - inteiro - 123456
float - real/ponto flutuante - 10.5 1.5 0.2
bool - booleano/lógico - true/false
"""
print('Texto', type('Texto'))
print(10, type(10))
print(25.26, type(25.26))
print(10 == 10, type(10 == 10))
print('L' == 'L', type('L' == 'L'))
print(bool(''))

