print('10', type('10'), type(int('10')))

# String: nome
print('Rafael', type('Rafael'))
# Inteiro: idade
print(23, type(23))
# Float: altura
print(1.70, type(1.70))
# Bool: é maior de idade 10 > 20
print(23 > 18, type(23 > 18))


